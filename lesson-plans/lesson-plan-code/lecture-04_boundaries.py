# Shared helpers
# --------------------------------------------------------------------------

def _class_traces(groups, feature_x, feature_y, feature_z, color_map, z_values):
    """Scatter3d trace per class. z_values(group) -> array of z coords."""
    return [
        go.Scatter3d(
            x=g[feature_x],
            y=g[feature_y],
            z=z_values(g),
            mode="markers",
            marker=dict(
                size=6,
                color=color_map[c],
                opacity=0.9,
                line=dict(color="black", width=1),
            ),
            name=str(c),
        )
        for c, g in groups.items()
    ]


def _default_color_map(categories):
    palette = px.colors.qualitative.Plotly
    return {c: palette[i % len(palette)] for i, c in enumerate(categories)}


def _single_color_surface(gx, gy, gz, color, opacity, visible, name, legend):
    return go.Surface(
        x=gx, y=gy, z=gz,
        colorscale=[[0, color], [1, color]],
        showscale=False,
        opacity=opacity,
        visible=visible,
        name=name,
        showlegend=legend,
        hoverinfo="skip",
    )


def _plane_grid(coef, intercept, bounds):
    """Grid for a*x + b*y + c*z + d = 0 clipped to the data box `bounds`.

    Solves over the dominant axes so near-vertical planes render too.
    """
    (x_min, x_max), (y_min, y_max), (z_min, z_max) = bounds
    a, b, c = coef
    d = intercept
    if abs(c) >= max(abs(a), abs(b)) * 1e-6 and abs(c) > 0:
        gx, gy = np.meshgrid(
            np.linspace(x_min, x_max, 30), np.linspace(y_min, y_max, 30)
        )
        gz = (-d - a * gx - b * gy) / c
        gz = np.where((gz >= z_min) & (gz <= z_max), gz, np.nan)
    elif abs(b) >= abs(a):
        gx, gz = np.meshgrid(
            np.linspace(x_min, x_max, 30), np.linspace(z_min, z_max, 30)
        )
        gy = (-d - a * gx - c * gz) / b
        gy = np.where((gy >= y_min) & (gy <= y_max), gy, np.nan)
    else:
        gy, gz = np.meshgrid(
            np.linspace(y_min, y_max, 30), np.linspace(z_min, z_max, 30)
        )
        gx = (-d - b * gy - c * gz) / a
        gx = np.where((gx >= x_min) & (gx <= x_max), gx, np.nan)
    return gx, gy, gz

def _padded_range(lo, hi, pad_frac=0.05):
    """Return a padded numeric axis range. Handles constant-valued columns."""
    lo = float(lo)
    hi = float(hi)
    span = hi - lo

    if span == 0:
        pad = abs(lo) * pad_frac if lo != 0 else 1.0
    else:
        pad = span * pad_frac

    return (lo - pad, hi + pad)

def _tree_rectangles(model, bounds):
    """All splitting rectangles of a fitted sklearn tree.

    Walks the tree tracking the bounding box each node governs. Each
    internal split (feature f at threshold thr) becomes an axis-aligned
    rectangle: f pinned at thr, the other two axes spanning that node's
    current box. Returns a list of (gx, gy, gz) grids.
    """
    t = model.tree_
    grids = []

    def recurse(node, box):
        f = t.feature[node]
        if f < 0:  # leaf
            return
        thr = float(t.threshold[node])
        u, v = (i for i in range(3) if i != f)
        gu, gv = np.meshgrid(
            np.linspace(box[u][0], box[u][1], 2),
            np.linspace(box[v][0], box[v][1], 2),
        )
        coords = [None, None, None]
        coords[f] = np.full_like(gu, thr)
        coords[u], coords[v] = gu, gv
        grids.append(tuple(coords))
        left = [b[:] for b in box]
        right = [b[:] for b in box]
        left[f][1] = thr
        right[f][0] = thr
        recurse(t.children_left[node], left)
        recurse(t.children_right[node], right)

    recurse(0, [list(b) for b in bounds])
    return grids


def _separator_surfaces(spec, bounds, default_name, visible):
    """Turn a separator spec into a list of Surface traces.

    Accepted specs:
      * plane coefficients (a, b, c, d)  for  a*x + b*y + c*z + d = 0
      * dict {"coef": (a, b, c), "intercept": d, ...style...}
      * a fitted sklearn decision tree (anything with `.tree_`)
      * dict {"model": fitted_tree, ...style...}
    Style keys: color, opacity, name.
    """
    style = spec if isinstance(spec, dict) else {}
    if isinstance(spec, dict) and "model" in spec:
        kind, payload = "tree", spec["model"]
    elif hasattr(spec, "tree_"):
        kind, payload = "tree", spec
    elif isinstance(spec, dict) and "coef" in spec:
        kind, payload = "plane", (spec["coef"], spec["intercept"])
    elif np.shape(spec) == (4,):
        kind, payload = "plane", (spec[:3], spec[3])
    else:
        raise ValueError(
            f"Can't interpret separator spec {spec!r}: expected plane "
            "coefficients (a, b, c, d), a dict with 'coef'/'intercept', a "
            "fitted decision tree, or a dict with 'model'."
        )

    name = style.get("name", default_name)
    if kind == "plane":
        coef, intercept = payload
        grids = [_plane_grid(coef, intercept, bounds)]
        color = style.get("color", "gray")
        opacity = style.get("opacity", 0.45)
    else:
        grids = _tree_rectangles(payload, bounds)
        color = style.get("color", "darkorange")
        opacity = style.get("opacity", 0.4)

    return [
        _single_color_surface(
            *g, color=color, opacity=opacity, visible=visible,
            name=name, legend=(k == 0),  # one legend entry per separator
        )
        for k, g in enumerate(grids)
    ]

def _scene_layout(fig, title, feature_x, feature_y, feature_z, x_range, y_range, z_range, buttons):
    fig.update_layout(
        title=title,
        scene=dict(
            xaxis_title=feature_x.replace("_", " "),
            yaxis_title=feature_y.replace("_", " "),
            zaxis_title=feature_z.replace("_", " "),
            xaxis=dict(range=list(x_range)),
            yaxis=dict(range=list(y_range)),
            zaxis=dict(range=list(z_range)),
            aspectmode="cube",
            camera=dict(eye=dict(x=1.5, y=1.5, z=1.3)),
        ),
        updatemenus=[
            dict(
                type="buttons",
                direction="left",
                buttons=buttons,
                pad=dict(r=10, t=10),
                x=0.1, xanchor="right",
                y=0, yanchor="bottom",
            )
        ],
        legend=dict(
            x=0.01, xanchor="left",
            y=0.99, yanchor="top",
            bgcolor="rgba(255, 255, 255, 0.5)",
        ),
        margin=dict(l=0, r=0, b=50, t=40),
    )


def _jump_args(frame_name):
    return [
        [frame_name],
        dict(
            frame=dict(duration=0, redraw=True),
            transition=dict(duration=0),
            mode="immediate",
        ),
    ]


def _bounds(df, feature_x, feature_y, feature_z):
    return [
        (df[feature_x].min(), df[feature_x].max()),
        (df[feature_y].min(), df[feature_y].max()),
        (df[feature_z].min(), df[feature_z].max()),
    ]


