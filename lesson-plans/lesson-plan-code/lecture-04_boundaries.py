def boundary_plot_compare_2d(
    df,
    feature_x,
    feature_y,
    target,
    separator_a,
    separator_b,
    title=None,
    color_map=None,
):
    """Points stay in 2D; a button toggles between two separators.

    Each separator can independently be a line or a fitted decision tree:
      * line coefficients (a, b, c)  for  a*x + b*y + c = 0
        (sklearn: a, b = model.coef_[0], c = model.intercept_[0])
      * dict {"coef": (a, b), "intercept": c, "color": ..., "opacity": ...,
              "name": ..., "width": ..., "dash": ...}
      * a fitted sklearn DecisionTree(Classifier/Regressor) -- or anything
        with a compatible `.tree_` -- trained on exactly
        [feature_x, feature_y] IN THAT ORDER
      * dict {"model": fitted_tree, "color": ..., "opacity": ..., "name": ...}

    separator_a is shown first; the button swaps to separator_b and back.
    """
    categories = list(df[target].unique())
    color_map = color_map or _default_color_map(categories)
    groups = {c: df[df[target] == c] for c in categories}
    bounds = [
        (df[feature_x].min(), df[feature_x].max()),
        (df[feature_y].min(), df[feature_y].max()),
    ]

    # Points are static: drawn once at their true coordinates.
    fig = go.Figure(data=_class_traces_2d(groups, feature_x, feature_y, color_map))

    traces_a = _separator_lines(separator_a, bounds, "Separator A", visible=True)
    traces_b = _separator_lines(separator_b, bounds, "Separator B", visible=False)
    idx_a = list(range(len(fig.data), len(fig.data) + len(traces_a)))
    for tr in traces_a:
        fig.add_trace(tr)
    idx_b = list(range(len(fig.data), len(fig.data) + len(traces_b)))
    for tr in traces_b:
        fig.add_trace(tr)

    # Two frames flipping which separator's traces are visible. The class
    # scatter traces are not in `traces`, so the points never move.
    trace_ids = idx_a + idx_b

    def frame_data(show_a):
        return (
            [go.Scatter(visible=show_a) for _ in idx_a]
            + [go.Scatter(visible=not show_a) for _ in idx_b]
        )

    fig.frames = [
        go.Frame(name="a", traces=trace_ids, data=frame_data(show_a=True)),
        go.Frame(name="b", traces=trace_ids, data=frame_data(show_a=False)),
    ]

    name_a = traces_a[0].name
    name_b = traces_b[0].name

    x_range = _padded_range(*bounds[0])
    y_range = _padded_range(*bounds[1])

    _scene_layout_2d(
        fig,
        title or f"{name_a} vs {name_b}",
        feature_x,
        feature_y,
        x_range,
        y_range,
        buttons=[
            dict(label=f"Show {name_a}", method="animate", args=_jump_args("a")),
            dict(label=f"Show {name_b}", method="animate", args=_jump_args("b")),
        ],
    )

    fig.show()


