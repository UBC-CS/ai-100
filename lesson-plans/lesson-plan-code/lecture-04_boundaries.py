def _class_traces_2d(groups, feature_x, feature_y, color_map):
    """One Scatter (2D) trace per class, at the points' true coordinates."""
    return [
        go.Scatter(
            x=g[feature_x],
            y=g[feature_y],
            mode="markers",
            marker=dict(
                size=8,
                color=color_map[c],
                opacity=0.9,
                line=dict(color="black", width=1),
            ),
            name=str(c),
        )
        for c, g in groups.items()
    ]


def _line_xy(coef, intercept, bounds):
    """Sampled points of a*x + b*y + c = 0 clipped to the data box.

    Solves over the dominant axis (NaN-masking the out-of-box part) so
    near-vertical lines render correctly.
    """
    (x_min, x_max), (y_min, y_max) = bounds
    a, b = coef
    c = intercept
    if abs(b) >= abs(a) and abs(b) > 0:
        xs = np.linspace(x_min, x_max, 60)
        ys = (-c - a * xs) / b
        ys = np.where((ys >= y_min) & (ys <= y_max), ys, np.nan)
    else:
        ys = np.linspace(y_min, y_max, 60)
        xs = (-c - b * ys) / a
        xs = np.where((xs >= x_min) & (xs <= x_max), xs, np.nan)
    return xs, ys


def _tree_segments_2d(model, bounds):
    """All splitting segments of a fitted 2-feature sklearn tree.

    Each internal split (feature f at threshold thr) becomes an
    axis-aligned segment: f pinned at thr, the other axis spanning that
    node's current box. Returns a list of (xs, ys) two-point segments.
    """
    t = model.tree_
    segs = []

    def recurse(node, box):
        f = t.feature[node]
        if f < 0:  # leaf
            return
        thr = float(t.threshold[node])
        if f == 0:  # vertical line x = thr, spanning the node's y-range
            xs, ys = [thr, thr], [box[1][0], box[1][1]]
        else:       # horizontal line y = thr, spanning the node's x-range
            xs, ys = [box[0][0], box[0][1]], [thr, thr]
        segs.append((xs, ys))
        left = [b[:] for b in box]
        right = [b[:] for b in box]
        left[f][1] = thr
        right[f][0] = thr
        recurse(t.children_left[node], left)
        recurse(t.children_right[node], right)

    recurse(0, [list(b) for b in bounds])
    return segs


def _separator_lines(spec, bounds, default_name, visible):
    """Turn a 2D separator spec into a list of Scatter line traces.

    Accepted specs:
      * line coefficients (a, b, c)  for  a*x + b*y + c = 0
        (sklearn: a, b = model.coef_[0], c = model.intercept_[0])
      * dict {"coef": (a, b), "intercept": c, ...style...}
      * a fitted sklearn decision tree (anything with `.tree_`) trained on
        exactly [feature_x, feature_y] IN THAT ORDER
      * dict {"model": fitted_tree, ...style...}
    Style keys: color, opacity, name, width, dash.
    """
    style = spec if isinstance(spec, dict) else {}
    if isinstance(spec, dict) and "model" in spec:
        kind, payload = "tree", spec["model"]
    elif hasattr(spec, "tree_"):
        kind, payload = "tree", spec
    elif isinstance(spec, dict) and "coef" in spec:
        kind, payload = "line", (spec["coef"], spec["intercept"])
    elif np.shape(spec) == (3,):
        kind, payload = "line", (spec[:2], spec[2])
    else:
        raise ValueError(
            f"Can't interpret separator spec {spec!r}: expected line "
            "coefficients (a, b, c), a dict with 'coef'/'intercept', a "
            "fitted decision tree, or a dict with 'model'."
        )

    name = style.get("name", default_name)
    if kind == "line":
        coef, intercept = payload
        segments = [_line_xy(coef, intercept, bounds)]
        color = style.get("color", "gray")
        width = style.get("width", 4)
    else:
        segments = _tree_segments_2d(payload, bounds)
        color = style.get("color", "darkorange")
        width = style.get("width", 3)

    return [
        go.Scatter(
            x=xs, y=ys, mode="lines",
            line=dict(color=color, width=width, dash=style.get("dash", "solid")),
            opacity=style.get("opacity", 1.0),
            visible=visible,
            name=name,
            showlegend=(k == 0),  # one legend entry per separator
            hoverinfo="skip",
        )
        for k, (xs, ys) in enumerate(segments)
    ]


def _scene_layout_2d(fig, title, feature_x, feature_y, x_range, y_range, buttons):
    fig.update_layout(
        title=dict(text=title, x=0, xanchor="left", y=0.97, yanchor="top"),
        xaxis=dict(title=feature_x.replace("_", " "), range=list(x_range)),
        yaxis=dict(title=feature_y.replace("_", " "), range=list(y_range)),
        updatemenus=[
            dict(
                type="buttons",
                direction="left",
                buttons=buttons,
                pad=dict(r=6, t=6),
                x=1, xanchor="right",   # top-right, away from the title
                y=1.12, yanchor="bottom",  # above the plot, not on the axis
                showactive=True,
            )
        ],
        legend=dict(
            x=0.01, xanchor="left",
            y=0.99, yanchor="top",
            bgcolor="rgba(255, 255, 255, 0.5)",
        ),
        margin=dict(l=60, r=20, b=60, t=90),  # t=90 makes room for title + buttons
    )

