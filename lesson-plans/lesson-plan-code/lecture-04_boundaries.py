import numpy as np
import plotly.graph_objects as go
import plotly.express as px


"""
Code generated by Claude Fable 5 and Opus 4.8, with some manual cleanup. Intended for use in lecture 4.

Interactive 3D teaching plots for class-separability demos.

Three public functions:

* boundary_plot_compare_2d -- points stay in 2D; a button toggles
  between two separators, each of which may be a line or a fitted
  decision tree.

* boundary_plot_2d_to_3d -- points toggle between a flat "2D view" and the
  full 3D view, with an optional 2D line (flat state) and 3D plane (risen
  state).

* boundary_plot_compare_3d -- points stay in 3D; a button toggles
  between two separators, each of which may be a plane or a fitted
  decision tree.
"""

