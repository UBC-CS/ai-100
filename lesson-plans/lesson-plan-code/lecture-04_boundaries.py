def boundary_plot_2d_to_3d(
    df,
    feature_x,
    feature_y,
    feature_z,
    target,
    title=None,
    color_map=None,
    line=None,
    plane=None
):
    categories = list(df[target].unique())
    color_map = color_map or _default_color_map(categories)
    groups = {c: df[df[target] == c] for c in categories}
    bounds = _bounds(df, feature_x, feature_y, feature_z)

    x_range = _padded_range(*bounds[0])
    y_range = _padded_range(*bounds[1])
    z_range = _padded_range(*bounds[2])

    z_min = bounds[2][0]

    flat_z = lambda g: np.full(len(g), z_min)

    fig = go.Figure(
        data=_class_traces(
            groups,
            feature_x,
            feature_y,
            feature_z,
            color_map,
            z_values=flat_z
        )
    )

    n_classes = len(categories)

    line_idx = None
    if line is not None:
        spec = line if isinstance(line, dict) else {"start": line[0], "end": line[1]}
        (lx0, ly0), (lx1, ly1) = spec["start"], spec["end"]

        line_idx = len(fig.data)
        fig.add_trace(
            go.Scatter3d(
                x=[lx0, lx1],
                y=[ly0, ly1],
                z=[z_min, z_min],
                mode="lines",
                line=dict(
                    color=spec.get("color", "black"),
                    width=spec.get("width", 6),
                    dash=spec.get("dash", "dash"),
                ),
                visible=True,
                name=spec.get("name", "2D boundary"),
                showlegend="name" in spec,
            )
        )

    plane_idx = None
    if plane is not None:
        plane_idx = len(fig.data)
        for tr in _separator_surfaces(plane, bounds, "3D boundary", visible=False):
            fig.add_trace(tr)

    trace_ids = list(range(n_classes))
    if line_idx is not None:
        trace_ids.append(line_idx)
    if plane_idx is not None:
        trace_ids.append(plane_idx)

    def frame_data(risen):
        z_values = (lambda g: g[feature_z]) if risen else flat_z

        data = _class_traces(
            groups,
            feature_x,
            feature_y,
            feature_z,
            color_map,
            z_values=z_values
        )

        if line_idx is not None:
            data.append(go.Scatter3d(visible=not risen))

        if plane_idx is not None:
            data.append(go.Surface(visible=risen))

        return data

    fig.frames = [
        go.Frame(name="flat", traces=trace_ids, data=frame_data(risen=False)),
        go.Frame(name="rise", traces=trace_ids, data=frame_data(risen=True)),
    ]

    _scene_layout(
        fig,
        title or f"{feature_x} & {feature_y}, then adding {feature_z}",
        feature_x,
        feature_y,
        feature_z,
        x_range,
        y_range,
        z_range,

        buttons=[
            dict(
                label="Show 3D",
                method="animate",
                args=_jump_args("rise"),
            ),
            dict(
                label="Show 2D",
                method="animate",
                args=_jump_args("flat"),
            ),
        ],
    )

    fig.show()


